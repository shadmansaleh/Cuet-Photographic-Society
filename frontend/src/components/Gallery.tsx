export function Gallery(images: string[], prefcols: number | undefined) {
  const [cols, setCols] = useState(prefcols || 4);
  let images_cols: Array<string[]> = new Array(cols);

  useEffect(() => {
    if (prefcols) return;
    const handleResize = () => {
      const screenWidth = window.innerWidth;
      if (screenWidth < 768) setCols(1);
      else if (screenWidth < 1024) setCols(2);
      else if (screenWidth < 1280) setCols(3);
      else setCols(4);
    };
    handleResize();
    window.addEventListener("resize", handleResize);
    return () => {
      window.removeEventListener("resize", handleResize);
    };
  }, []);

  images.forEach((image, index) => {
    if (images_cols[index % cols] === undefined) images_cols[index % cols] = [];
    images_cols[index % cols].push(image);
  });

  return (
    <div className={`grid grid-cols-${cols} gap-4`}>
      {images_cols.map((rows, index) => (
        <div key={`gallery-col-${index}`} className="grid gap-4">
          {rows.map((image, index) => (
            <img
              key={`gallery-image-${index}`}
              className="h-auto max-w-full rounded-lg object-cover object-center mx-auto w-screen"
              src={image}
              alt="gallery-photo"
            />
          ))}
        </div>
      ))}
    </div>
  );
}

export default Gallery;
